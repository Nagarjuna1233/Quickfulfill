package com.quickfulfill.dao.pojo;

import java.io.Serializable;
import java.util.Date;

public class Logistics implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String logisticId;
	private String logisticName;
	private String workingDays;
	private boolean scheduled;
	private boolean active;
	private String slotFrom;
	private String slotTo;
	private int id;
	private Date creationTime;
	private Date modifiedTime;
	public Logistics(String logisticId, String logisticName,
			String workingDays, boolean scheduled, boolean active,
			String slotFrom, String slotTo, int id, Date creationTime,
			Date modifiedTime) {
		super();
		this.logisticId = logisticId;
		this.logisticName = logisticName;
		this.workingDays = workingDays;
		this.scheduled = scheduled;
		this.active = active;
		this.slotFrom = slotFrom;
		this.slotTo = slotTo;
		this.id = id;
		this.creationTime = creationTime;
		this.modifiedTime = modifiedTime;
	}
	public String getLogisticId() {
		return logisticId;
	}
	public void setLogisticId(String logisticId) {
		this.logisticId = logisticId;
	}
	public String getLogisticName() {
		return logisticName;
	}
	public void setLogisticName(String logisticName) {
		this.logisticName = logisticName;
	}
	public String getWorkingDays() {
		return workingDays;
	}
	public void setWorkingDays(String workingDays) {
		this.workingDays = workingDays;
	}
	public boolean isScheduled() {
		return scheduled;
	}
	public void setScheduled(boolean scheduled) {
		this.scheduled = scheduled;
	}
	public boolean isActive() {
		return active;
	}
	public void setActive(boolean active) {
		this.active = active;
	}
	public String getSlotFrom() {
		return slotFrom;
	}
	public void setSlotFrom(String slotFrom) {
		this.slotFrom = slotFrom;
	}
	public String getSlotTo() {
		return slotTo;
	}
	public void setSlotTo(String slotTo) {
		this.slotTo = slotTo;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Date getCreationTime() {
		return creationTime;
	}
	public void setCreationTime(Date creationTime) {
		this.creationTime = creationTime;
	}
	public Date getModifiedTime() {
		return modifiedTime;
	}
	public void setModifiedTime(Date modifiedTime) {
		this.modifiedTime = modifiedTime;
	}
	
}
