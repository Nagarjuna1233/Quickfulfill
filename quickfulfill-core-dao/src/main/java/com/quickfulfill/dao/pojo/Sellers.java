package com.quickfulfill.dao.pojo;

import java.io.Serializable;
import java.util.Date;

public class Sellers implements Serializable {

	private static final long serialVersionUID = 1L;
  
	private String sellerId;
	private String sellerName;
	private String address1;
	private String address2;
	private String city;
	private String state;
	private String pincode;
	private String sellerHandlingTime;
	private boolean TSHIP;
	private boolean SSHIP;
	private String primaryFulfillment;
	private boolean Active;
	private int id;
	private Date creationTime;
	private Date modifiedTime;
	public Sellers(String sellerId, String sellerName, String address1,
			String address2, String city, String state, String pincode,
			String sellerHandlingTime, boolean tSHIP, boolean sSHIP,
			String primaryFulfillment, boolean active, int id,
			Date creationTime, Date modifiedTime) {
		super();
		this.sellerId = sellerId;
		this.sellerName = sellerName;
		this.address1 = address1;
		this.address2 = address2;
		this.city = city;
		this.state = state;
		this.pincode = pincode;
		this.sellerHandlingTime = sellerHandlingTime;
		TSHIP = tSHIP;
		SSHIP = sSHIP;
		this.primaryFulfillment = primaryFulfillment;
		Active = active;
		this.id = id;
		this.creationTime = creationTime;
		this.modifiedTime = modifiedTime;
	}
	public String getSellerId() {
		return sellerId;
	}
	public void setSellerId(String sellerId) {
		this.sellerId = sellerId;
	}
	public String getSellerName() {
		return sellerName;
	}
	public void setSellerName(String sellerName) {
		this.sellerName = sellerName;
	}
	public String getAddress1() {
		return address1;
	}
	public void setAddress1(String address1) {
		this.address1 = address1;
	}
	public String getAddress2() {
		return address2;
	}
	public void setAddress2(String address2) {
		this.address2 = address2;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getPincode() {
		return pincode;
	}
	public void setPincode(String pincode) {
		this.pincode = pincode;
	}
	public String getSellerHandlingTime() {
		return sellerHandlingTime;
	}
	public void setSellerHandlingTime(String sellerHandlingTime) {
		this.sellerHandlingTime = sellerHandlingTime;
	}
	public boolean isTSHIP() {
		return TSHIP;
	}
	public void setTSHIP(boolean tSHIP) {
		TSHIP = tSHIP;
	}
	public boolean isSSHIP() {
		return SSHIP;
	}
	public void setSSHIP(boolean sSHIP) {
		SSHIP = sSHIP;
	}
	public String getPrimaryFulfillment() {
		return primaryFulfillment;
	}
	public void setPrimaryFulfillment(String primaryFulfillment) {
		this.primaryFulfillment = primaryFulfillment;
	}
	public boolean isActive() {
		return Active;
	}
	public void setActive(boolean active) {
		Active = active;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Date getCreationTime() {
		return creationTime;
	}
	public void setCreationTime(Date creationTime) {
		this.creationTime = creationTime;
	}
	public Date getModifiedTime() {
		return modifiedTime;
	}
	public void setModifiedTime(Date modifiedTime) {
		this.modifiedTime = modifiedTime;
	}
	
	
}
